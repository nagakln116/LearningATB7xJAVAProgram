package jul.ex_13072024;

public class Lab015 {
    public static void main(String[] args) {
        //Data type
            //Primitive data type
                //Boolean true/false
                boolean is_married = true;
                boolean are_you_a_monkey = false;
                System.out.println(is_married);
                System.out.println(are_you_a_monkey);
                //Numeric
                    //Integral - byte, short, int, long
                    byte b = 108;
                    short s = 10001;
                    int i = 960006403;
                    long l = 9600064039l;
                    System.out.println(b);
                    System.out.println(s);
                    System.out.println(i);
                    System.out.println(l);
                    //float - float, double
                    float f = 9.123456f; //rounds of after 7th decimal
                    System.out.println(f);
                    double d = 118.1234567890123456789; //rounds of after 14th decimal
                    System.out.println(d);
                    //char - A,B
                    char c = 'A';
                    System.out.println(c);
            //Non Primitive data type
                //String - collection of char
                String name = "Naga";
                System.out.println(name);

    }
}
