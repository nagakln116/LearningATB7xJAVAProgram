package jul.ex_13072024;

public class Lab041 {
    public static void main (String[] args ) {
        int i = 10;
        i = i + 5;

        //Compound operators
        System.out.println(i);
        i+=2;
        System.out.println(i);
        i-=2;
        System.out.println(i);
        i*=2;
        System.out.println(i);
        i/=2;
        System.out.println(i);

    }
}
